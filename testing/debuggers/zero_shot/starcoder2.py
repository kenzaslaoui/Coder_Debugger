"""
starcoder2.py: Zero-shot debugger using StarCoder 2
"""
import torch
from transformers import AutoModelForCausalLM, AutoTokenizer

class StarCoderDebugger:
    def __init__(self):
        self.model_name = "bigcode/starcoder2-7b"

        # Initialize model
        self.device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
        print(f"Using device: {self.device}")

        # Load tokenizer and model
        self.tokenizer = AutoTokenizer.from_pretrained(self.model_name)

        if self.tokenizer.pad_token is None:
            self.tokenizer.pad_token = self.tokenizer.eos_token

        print(f"Loading model: {self.model_name}")
        self.model = AutoModelForCausalLM.from_pretrained(
            self.model_name,
            torch_dtype=torch.bfloat16,
            device_map="auto"
        )
        self.model.eval()

        print("StarCoder model loaded successfully!")

    def debug_code(self, task: str, buggy_code: str, max_new_tokens: int = 512) -> str:
        """Zero-shot debugging (no error message)"""
        # Format prompt for zero-shot debugging
        full_prompt = self.format_debug_prompt(task, buggy_code)

        try:
            # Prepare input
            inputs = self.tokenizer(full_prompt, return_tensors="pt")
            inputs = {k: v.to(self.device) for k, v in inputs.items()}

            # Generate response
            with torch.no_grad():
                outputs = self.model.generate(
                    inputs.input_ids,
                    max_new_tokens=max_new_tokens,
                    do_sample=False,
                    top_k=50,
                    top_p=0.95,
                    temperature=0.2,
                    pad_token_id=self.tokenizer.eos_token_id
                )

            # Decode response
            fixed_code = self.tokenizer.decode(outputs[0][inputs.input_ids.shape[1]:], skip_special_tokens=True)
            return fixed_code.strip()
        except Exception as e:
            print(f"Error in generation: {e}")
            return f"Error in generation: {e}"

    def format_debug_prompt(self, task: str, buggy_code: str) -> str:
        """Format prompt for zero-shot debugging with StarCoder"""
        return (
            f"# Task: {task}\n\n"
            f"# Buggy code:\n{buggy_code}\n\n"
            f"# Fixed code:\n\n"
        )